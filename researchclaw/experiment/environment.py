"""Experiment environment capture for reproducibility (P4.2).

Collects Python/pip/CUDA/GPU/platform metadata at experiment time,
and generates requirements.txt for reproducible results.

Usage::

    from researchclaw.experiment.environment import capture_environment
    env_info = capture_environment()
    # env_info["python_version"], env_info["pip_packages"], etc.
"""

from __future__ import annotations

import logging
import platform
import subprocess
import sys
import time
from pathlib import Path
from typing import Any

from researchclaw.utils.env import minimal_subprocess_env

logger = logging.getLogger(__name__)


def _run_cmd(cmd: list[str], timeout: int = 15) -> str:
    """Run a shell command and return stripped stdout, or '' on error."""
    try:
        result = subprocess.run(
            cmd,
            capture_output=True,
            text=True,
            timeout=timeout,
            env=minimal_subprocess_env(),
        )
        return result.stdout.strip()
    except Exception:
        return ""


def capture_environment(sandbox: Any | None = None) -> dict[str, Any]:
    """Collect full environment metadata for experiment reproducibility.

    Returns a dictionary with:
    - python_version: str
    - platform: str
    - machine: str
    - system: str
    - pip_packages: list[str] (top-level packages from pip freeze)
    - torch_version: str | None
    - cuda_version: str | None (from torch.version.cuda)
    - cuda_driver_version: str | None (from nvidia-smi)
    - gpu_info: list[dict] | None (GPU name, VRAM, driver)
    - captured_at: str (ISO timestamp)
    """
    info: dict[str, Any] = {
        "python_version": sys.version,
        "platform": platform.platform(),
        "machine": platform.machine(),
        "system": platform.system(),
        "captured_at": time.strftime("%Y-%m-%dT%H:%M:%SZ", time.gmtime()),
    }

    # Pip packages
    pip_output = _run_cmd([sys.executable, "-m", "pip", "freeze"], timeout=30)
    if pip_output:
        packages = [line.strip() for line in pip_output.splitlines()
                     if line.strip() and not line.startswith("#")]
        info["pip_packages"] = packages
    else:
        info["pip_packages"] = []

    # PyTorch version and CUDA version
    try:
        import importlib
        torch_spec = importlib.util.find_spec("torch")
        if torch_spec is not None:
            import torch
            info["torch_version"] = torch.__version__
            if hasattr(torch.version, "cuda") and torch.version.cuda:
                info["cuda_version"] = torch.version.cuda
            else:
                info["cuda_version"] = None
        else:
            info["torch_version"] = None
            info["cuda_version"] = None
    except Exception:
        info["torch_version"] = None
        info["cuda_version"] = None

    # CUDA driver version via nvidia-smi
    driver_ver = _run_cmd([
        "nvidia-smi", "--query-gpu=driver_version",
        "--format=csv,noheader",
    ], timeout=10)
    if driver_ver:
        info["cuda_driver_version"] = driver_ver
    else:
        info["cuda_driver_version"] = None

    # GPU detailed info
    gpu_query = _run_cmd([
        "nvidia-smi",
        "--query-gpu=name,memory.total,driver_version,compute_cap",
        "--format=csv,noheader",
    ], timeout=10)
    if gpu_query:
        gpus = []
        for line in gpu_query.splitlines():
            parts = [p.strip() for p in line.split(",")]
            if len(parts) >= 4:
                gpus.append({
                    "name": parts[0],
                    "memory_total": parts[1],
                    "driver_version": parts[2],
                    "compute_cap": parts[3],
                })
        info["gpu_info"] = gpus
    else:
        info["gpu_info"] = None

    logger.debug(
        "Captured environment: python=%s, torch=%s, cuda=%s, gpus=%s",
        info.get("python_version", "")[:30],
        info.get("torch_version", "N/A"),
        info.get("cuda_version", "N/A"),
        len(info.get("gpu_info") or []),
    )

    return info


def generate_requirements_txt(environment: dict[str, Any] | None = None) -> str:
    """Generate a requirements.txt from captured environment or current state.

    Returns the requirements.txt content as a string.
    """
    env = environment or capture_environment()
    packages = env.get("pip_packages", [])
    if not packages:
        # Fallback: capture fresh
        pip_output = _run_cmd([sys.executable, "-m", "pip", "freeze"], timeout=30)
        if pip_output:
            packages = [line.strip() for line in pip_output.splitlines()
                         if line.strip() and not line.startswith("#")]

    if not packages:
        return "# No pip packages detected\n"

    lines = [
        "# Auto-generated by ResearchClaw — experiment reproducibility (P4.2)",
        f"# Generated: {env.get('captured_at', 'unknown')}",
        f"# Python: {env.get('python_version', 'unknown')[:40]}",
        f"# Platform: {env.get('platform', 'unknown')}",
        "",
    ]
    lines.extend(packages)
    return "\n".join(lines) + "\n"


def generate_environment_yaml(environment: dict[str, Any] | None = None) -> str:
    """Generate a Conda-style environment.yaml from captured metadata.

    Note: This is a lightweight approximation — use conda env export for full fidelity.
    """
    try:
        import yaml
    except ImportError:
        return "# PyYAML not available for environment.yaml generation\n"

    env = environment or capture_environment()

    doc: dict[str, Any] = {
        "name": "researchclaw-experiment",
        "channels": ["conda-forge", "defaults"],
        "dependencies": ["python=" + platform.python_version()],
    }

    # Add pip packages
    packages = env.get("pip_packages", [])
    if packages:
        doc["dependencies"].append({"pip": packages})

    metadata = {
        "platform": env.get("platform", ""),
        "machine": env.get("machine", ""),
        "torch_version": env.get("torch_version", "N/A"),
        "cuda_version": env.get("cuda_version", "N/A"),
        "captured_at": env.get("captured_at", ""),
    }
    doc["metadata"] = metadata

    return (
        "# Auto-generated by ResearchClaw — experiment reproducibility\n"
        + yaml.dump(doc, default_flow_style=False, sort_keys=False)
    )


def write_reproducibility_artifacts(
    output_dir: Path,
    environment: dict[str, Any] | None = None,
) -> dict[str, Path]:
    """Write reproducibility artifacts to an output directory.

    Creates:
    - environment.json (full metadata)
    - requirements.txt (pip packages)

    Returns a dict of artifact_name -> file_path.
    """
    env = environment or capture_environment()
    output_dir.mkdir(parents=True, exist_ok=True)

    try:
        import json

        env_path = output_dir / "environment.json"
        env_path.write_text(
            json.dumps(env, indent=2, ensure_ascii=False, default=str),
            encoding="utf-8",
        )

        req_path = output_dir / "requirements.txt"
        req_path.write_text(generate_requirements_txt(env), encoding="utf-8")

        logger.info(
            "Reproducibility artifacts written to %s", output_dir
        )
        return {"environment_json": env_path, "requirements_txt": req_path}
    except Exception as exc:
        logger.warning("Failed to write reproducibility artifacts: %s", exc)
        return {}
